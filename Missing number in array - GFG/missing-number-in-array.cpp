class Solution{
  public:
    int MissingNumber(vector<int>& array, int n) {
        // Your code goes here
        int i,sum=0,c=0;
        for( i=0;i<n-1;i++)
        {
            c=c+i+1;
            sum=sum+array[i];
            
        }
        c=c+i+1;
        return c-sum;
    }
};

